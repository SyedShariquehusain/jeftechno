                <p><b>Increased use of flammable alcohol-based sanitizers</b><br>
                With the addition of Ventilators, air-conditioners, high-capacity machines in the ICUs, testing facilities for COVID, the addition of more rooms, and increased use of flammable alcohol-based sanitizers or PPE kits, risks of fire are only going higher. Add to it the poorly designed power systems built with even poorer workmanship, it all works like oxygen to start an electrical fire and feed it further.</p>
                <h3 style="color:#479fe0;">SOLUTION- BUILDING A PROACTIVE APPROACH TO FIRE SAFETY</h3>
               <p><b>Ensure compliance to building codes for safety and reliability</b><br>
                Fire in Covid-19 Hospitals - Prevention must be the goal Hospitals that use several non-linear loads and sensitive electronics alongside heavy-duty air conditioners and heaters, other large or small medical testing instruments, and are therefore always at the risk of poor power quality. While the first responsibility to ensure compliance to building codes for safety and reliability rests with designers.</p>
                <h5 style="color:#479fe0;">Electrical & Fire Safety Audit</h3><br>
               <p> Proper maintenance and monitoring of Power Quality on an ongoing basis is important. With rising number of electrical fires in hospitals, several state and regulatory agencies have ordered a fire audit of the facilities. While a proactive fire audit of the COVID hospitals is a welcome move, the fire audits are testing the ability of the facility to protect itself from the fire, if it were to occur, not prevent the fire.</p>
               <p><b>Wire your electrical networks right</b><br>
                    Poor quality cables could lead to a serious fire incident at a hospital. It is vital that attention be paid to the wiring in the entire health facility. The global norm is to use ‘Low Smoke and Zero Halogen (LSOH) wiring. However, in India, Flame Retardant Low Smoke (FRLS) wires are used widely. Reports suggest that these wires may not necessarily help avoid fire incidents completely.

                                                                                                                                                                                                                                                                                                                                            </p>
                
                <p><b>The ventilation system</b><br>
                    The ventilation system in a hospital also has a role to play in this scenario. If the hospital is air-conditioned, an efficient mechanical ventilation system must be installed to avoid electrical fires from spreading</p>                                                                                                                                                                                                                                                                                                                            <!-- <p> 4.To perform all the calculations manually and to arrive at the risk level might be a tedious task. You will need an expert to perform the task and you will have to invest both your time and money for the same. To make this task incredibly easy for you, we, at JEF, have come up with an automated tool to perform your RISK ASSESSMENT within seconds.</p> -->
              <h5 style="color:#479fe0;">Pay attention to earthing</h5>
                     <p>A common practice in the Indian setup is to use separate earthing connections for all the electronic medical or non-medical equipment, but it is unjustified.
                    </p>
                        All conductive metal in an equipotential area should be connected to a common equipotential earthing point with a special heavy-duty cable. This reduces the possibility of leakage currents that can cause micro electrocution when the surgeon or patient comes into contact with the equipment.

                                                                                                                                                                    </p>
