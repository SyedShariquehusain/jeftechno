              <h2>Electrical Fire in Covid-19 Hospitals A Preventable Emergency</h2>
            </div>
            <div class="post-media"> <img src="../assets/images/blog/ef1.png" alt=""><span class="event-calender blog-date"> 21 <span>june</span> </span> </div>
            <div class="post-entry">
              <p> Power Systems in hospital facilities are particularly vulnerable given the sudden increase in nonlinear electronic loads comprising of ventilators, diagnostic imaging equipment, Patient monitors and many bio medical devices – a crucial defense aid in fighting COVID. With improper electrical systems, further marred by non-compliance to electrical standards and poor workmanship, hospitals are staring at the high risk of overheating their electrical installation that could provoke fire. </p>
              <p>Fire Causes: <br>
                  Faulty electrical outlets and old, outdated equipment are the leading causes of electrical fires.Never use a device with a broken or damaged cable because it can cause a fire by sending heat to flammable surfaces including floors, drapes, and carpets. 
              </p>
              <p>Fire prevention: <br>
                  Around a socket, light switch, or light fixture, there may be burn marks or discoloration. When you touch an appliance, an outlet, or a power wire, it feels warm. When an object is plugged in or in operation, it emits a burning odor. When you plug in a gadget, electrical sparks appear. Each time you plug in a gadget, you get an electric jolt.
              </p>
              <h3 style="color:#479fe0;">REASON:Electrical fires causes and prevention</h3>
              <p><b>Poor electrical wiring and cabling</b><br>
             Power-hungry electrical equipment such as MRI, CT Scans, and X-Ray machines, Ventilators and other medical devices, used in hospitals need uninterrupted power supply. A durable cable network is thus absolutely essential to carry this power supply without overheating.</p>
             <p><b>Faulty electrical installations in an oxygen-rich environment</b><br>
                Areas such as operation theaters and ICUs have a high concentration of oxygen. Any electrical short circuit in these areas, could lead to electrical shock to surgeons or patients. In order to achieve uninterrupted power, or for surge control, UPS systems are used which are supported by batteries.

                                                                                                                                            </p>
             
