            <h3 style="color:#479fe0;">LOW VOLTAGE SPD- SELECTION & INSTALLATION - MYTHS VS FACTS.</h3>
            <p><a style="color:#479fe0;" href="https://www.jeftechno.com/blog/selection-of-low-voltage-apd-for-main-db.php"> Low voltage</a> is defined as any AC RMS voltage up to 1000 V or DC peak voltage up to 1500 V. This needs to be kept in mind because Medium voltage SPD- called as LA- Lightning Arrestor manufacturing techniques, selection and installation is totally different from low voltage SPDs

                In general, <br>SPDs need to be installed at main DB, sub DB and at equipment level as per Figure 1.</p>
                <img src="../assets/images/blog/picture11.png" alt=""/>
                <p><b>Low Voltage SPD- Selection & installation</b></p>
                <p><u>Figure 1-Example of installation of Class I, class II and class III tested SPDs</u></p>


               <p>In the above figure instead of Class I and Class II, Class I + II can be used if Iimp value is taken care

               <br> It is because<a style="color:#479fe0;" href="https://www.jeftechno.com/surge-protection-devices.php"> SPDs</a> provide an “effective” protection distance of a maximum of 10 meters. It means, if the distance between the SPD and the equipment to be protected is greater than 10 meters, additional SPDs will be required. The crucial point to note in this case is that the “actual” wire length needs to be calculated rather than the physical distance between the SPD and equipment.
                </p>
                <img src="../assets/images/blog/picture12.png" alt=""/>
                <p><u>Figure 2- Difference between physical distance and actual distance.</u></p>
                <p>Physical location of equipment is important to select the quantity of SPDs. In the below Figure 3- 2 examples are given. In Example1, the number of PCs are 5 & in Example 2, the number of PCs are 4.. But in Example 1, one SPD is enough to protect all the 5 PCs as the “actual ‘ distance from SPD to the farthest PC- PC 3 is only 5 meters, because all the PCs are kept in one room, next to each other.

                    Whereas. In Example 2, 4 number of SPDs are needed to protect 4 PCs , because 1 PC is located in each floor and the ‘actual” distance exceeds 10 m in Floors 1, 2 & 3.Only PC 1 does not require any additional SPD because the “actual’ distance from the SPD which is in Sub DB is only 5 meters away from PC1.</p>
                   <p><b>Fig 3-Example 1</b></p>
