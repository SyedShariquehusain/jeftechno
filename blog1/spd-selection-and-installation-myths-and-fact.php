                    <p><u>Figure 2- Difference between physical distance and actual distance.</u></p>
                    <p>The below selection principle shall be strictly followed:<br>

                        a) Protection between Live conductor and PE is COMPULSORY.<br>

                        b) Protection between Live conductor and neutral is RECOMMENDED to ensure equipment protection.<br>

                        c) Protection between Live Conductors ( 3 phase) is OPTIONAL.</p>
                   <p><b><u>Selection - MYTH:</u></b></p>
                   <p>Few SPD manufacturers strongly recommend costly SPDs assembled in big Stainless steel enclosures to protect critical installations without any back up from IEC standards. It is clear from the above recommendation that. Protection between Live Conductors is OPTIONAL.</p>

                   <p>Installation of three-phase LV SPD in 2-connection types- CT-1 (Connection Type 1) & CT-2 (Connection Type 2). In Connection Type, CT 1, TWO types of earthing arrangements are possible i.e., L1, L2, L3, N & PE (i.e., neutral and earth are separate wires & termed as N & PE) and L1, L2, L3 & PEN (i.e., Neutral and Earth are combined as one wire & termed as PEN). Only one sort of earthing arrangement is possible in Connection Type CT-2. There was previously some misunderstanding over whether to use CT1 or CT2, as both the IEC standard suggested.</p>   
                   <p><b><u>Selection - FACT:</u></b></p>
                   <p>In September 2015, the latest IEC standard, IEC 60364- 5-53 (Edition 3.2), was published. Electrical Installation of Buildings- Part 5-53- Selection and erection of electrical equipment- Isolation, switching, and control clearly outlines the concepts of LV SPD selection and installation.<br>

                   The CT-1 connection type big protects against common-mode attacks. (For example, between the Live and Neutral endpoints and the Neutral and Earth terminals). Concisely, PE is protected between L and N.<br>

                   The CT-2 connection type combines common-mode and differential-mode protection (i.e., between the Live and Earth terminals) (i.e., between Live & Neutral terminals).
