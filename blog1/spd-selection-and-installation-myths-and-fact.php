                        <h4>CONCLUSION:</h4>
                        <p>From the above, it is clear that selection & installation of SPD is a Specialist job as the installer shall have thorough knowledge on the current Indian & international standards along with hands-on experience because there are challenges related to each site. Again it is specialised because, most of the panel builders & technicians who install SPDs are conversant with MCB installations & follow the same practice, without reading the “installation manual” of the SPD manufacturer. If the above practices are followed, customers will have years of trouble free operation of their equipment & SPDs.</p>
                       
