                    <p>Please note that, if you observe closely, Connection Type CT2 is not possible for PEN- I.e combined Neutral & Protective earth, because if N& PE are combined, it automatically becomes CT 1 (3+0) configuration.

                        <br><br>Only connection type CT-2 need to be followed, due to the constraint in Voltage Protection level and the installation bottleneck. (explained in the next section)</p>
                        <p><b><u>Installation - MYTH:</u></b></p>
                        <p>In some installations, the person who installs the SPD is neither knowledgeable nor read the installation instructions of the SPD manufacturer with the result that, even after the installation of SPD, the equipment that is expected to get protection from SPD fails. This is due to the fact that, long length of cables is used to connect SPDs and connecting the SPD earth terminal to a separate earth which is not part of the equipment earth.</p>
                        <p><b><u>Installation - FACT:</u></b></p>
                        <P>The total length of the cable which includes cable length from live wire to OCPD- Over Current Protective Device I.e Fuse or MCB , from OCPD to SPD and from SPD to earth terminal has to be below 0.5 meter to ensure protection of equipment. Figures 7 & 8 below explains good installation techniques, which also takes care of wire thickness for Live conductors and PE terminals for Class I or Class I+II and Class II SPDs.</P>
