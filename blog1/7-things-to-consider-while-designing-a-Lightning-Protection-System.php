              <h2>7 THINGS TO CONSIDER WHILE DESIGNING A LIGHTNING PROTECTION SYSTEM</h2>
            </div>
            <div class="post-media"> <img src="../assets/images/blog/lightning.jpg" alt=""><span class="event-calender blog-date"> 21 <span>june</span> </span> </div>
            <div class="post-entry">
            <p>Let us quickly give you a basic idea about designing a Lightning Protection System, here are 7 important points.

            </p> 
           <h3 style="color:#479fe0;">1. FIRST AND FOREMOST IS COLLECTING THE ACCURATE SET OF DATA!</h3>
           <p>Accurate data plays a very important role in designing a lightning protection system and is the important deciding factor in the later stage of the design. So, the crucial data to be collected involves dimensions, location and environmental factors of the building/ structure. If the data is approximate or wrong, the same<br> will get reflected in the recommendation.</p>
            <h3 style="color:#479fe0;">2. WHAT DO YOU DO WITH THE “ACCURATE” DATA COLLECTED FOR A PARTICULAR BUILDING/STRUCTURE?</h3>
           <p>The next crucial step is to do a<a style="color:#479fe0;" href="https://www.jeftechno.com/JEF_PHP/registerdesign.php"> RISK ASSESSMENT! </a>It decides whether a building requires external LPS, SPDs, or both, or whether no external LPS, SPDs, or both are needed. If protection is necessary, calculate the level of risk by using the data collected as the input in our JEF SHIELD. JEF SHIELD is an automated software build as per IS/IEC 62305-2 to ease your risk calculation, and it is validated with the examples given in IS/IEC 62305-2.

          </p>
           <h3 style="color:#479fe0;">3. ARRIVING AT THE LEVEL OF PROTECTION.</h3>
           <p>If you do the risk assessment properly, by giving accurate data, then be confident that your level of protection is certainly correct!<br>

            Here there are few possibilities :<br>

            a. Whether the building/structure needs External Protection, which is your LPS.<br>

            b. Whether the building/structure needs Internal Protection, which is your SPDs.<br>

            c. Whether the building/structure needs both Internal & External Protection.<br>

            d. Whether the building/structure is ALREADY SAFE & its calculated values are below the tolerable levels & hence no protection is required</p>
           <h3 style="color:#479fe0;">4.</h3>
           <p>After you determine the level of protection, the next step is to design your lightning protection system based on the level of protection. There are four levels of protections under which a building/structure can fall under. Level I provides the highest level of protection and level IV provides the least level of protection.</p>
           <h3 style="color:#479fe0;">5.</h3>
           <p>After the level of protection is determined, the next interesting step is to choose the appropriate lightning protection method. There are 3 different methods :<br>

            a. Mesh Method<br>

            b. Protection angle method<br>

            c. Rolling sphere method<br>

                For an optimum and smart solution, you can combine the above mentioned methods and arrive at a solution. Rolling sphere method is stringent & needs to be followed in Industries & all critical installations.</p>
           <h3 style="color:#479fe0;">6.</h3>
                <p>Point number 6 is to specifically keep in mind to refer the Standard IS/IEC 62305 for:<br>

                a. Mesh Size<br>

                b. Rolling sphere radius<br>

                c. Protection angle<br>

                All the above parameters are based on your level of protection, so be smart and don’t make a mistake in that! If at all you choose the wrong level of protection for your building/structure, all your next steps would go wrong and your building/structure may not be safe during the event of lightning.</P>
                <h3 style="color:#479fe0;">7.</h3>
                <p>So, after you design your LPS, what do you do next?<br>

                You arrive at the bill of materials and estimate the cost for your Lightning protection system. And FINALLY your building/structure has a Lightning Protection System complying to Indian/International standards & norms.<br>

                And here after, you can have the lightning protections system installed and you do not have to worry about any lightning events affecting your building/structure!

