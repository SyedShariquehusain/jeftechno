                    <p>The complete system will include conductors, strike termination rods, ground terminals and inter-connecting bonding to minimize side-flashing and surge protection device to prevent harmful electrical surges in power, data and communication lines.</p>
                    <h3 style="color:blue;">INSTALLATION</h3>
                    <p>The SPDs are installed on the electrical service panels and on any other incoming lines – they serve as the first defence against the harmful electrical surges that may enter the building through power lines.
                        The lightning rods that protect the building from a direct lightning strike are installed at the highest point, such as on the roof. All buildings must have a minimum of two ground rods. The groundings must be set as wide apart as possible; normally in diagonally opposite corners.</p>
