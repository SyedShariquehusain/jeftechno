            <div class="post-media"> <img src="../assets/images/blog/How does a Lightning Protection System work.jpg" alt="" width="800" height="300"/><span class="event-calender blog-date"> 21 <span>june</span> </span> </div>
            <div class="post-entry">

            <p>Thunder storms occur almost everywhere in the world. A lightning strike has the potential to damage just about any type of structure. However, unlike most weather hazards, it’s possible to prevent lightning from wreaking havoc on your property.</p>
           <p>Although nothing can prevent lightning from striking, a lightning protection system will intercept the strike and disperse it safely into the ground – saving households and businesses from a great deal of upset and inconvenience.</p>
           <h3 style="color:blue;">DANGERS OF LIGHTNING</h3>
           <p>Lightning is a natural phenomenon that can produce voltages greater than 100 million volts, while bolting temperatures that are hotter than the surface of the sun. As one of the most dangerous forces on the earth, it is very capable of causing major and sometimes catastrophic damage.</p>

            <p>A lightning strike can create power surges, causing electricity to sweep through a property’s wiring or plumbing system. During a bad storm, valuable electronic equipment such as computers and televisions should be unplugged to avoid damage. If you happen to be running water and touching any electrical equipment, you could well be electrocuted.</p>

            <h3 style="color:blue;">FIRE RISKS</h3>
            <p>With a real risk of fire, the attic will commonly catch light when a lightning bolt comes through the roof. The heat from a lightning bolt will run through walls and can trigger electrical fire hazards that could either ignite immediately or smoulder inside the walls – this can sometimes go unnoticed until smoke starts billowing into the building.</p>

                <p>
                        In the case of business premises, lightning can cause severe disruption and loss of income if the building is either destroyed or left unsafe to use. For both domestic and business premises, an insurance claim can cause future premiums to increase. Tall buildings and monuments are often hit by lightning – churches with tall spires, clock towers, electricity pylons and trees are all at risk. The Empire State Building is struck by lightning approximately 100 times each year!

                </p>

                <h3 style="color:blue;">LIGHTNING RODS</h3>
                <p>In a lightning protection system, strike termination rods in the shape of lightning rods are positioned in the most exposed extremities and on the highest parts of a structure at regular intervals – they become the most likely point of contact for the lightning, creating the quickest path to the ground.

                                    </P>
                <p>Although it doesn’t prevent a lightning strike, a lightning protection system rather provides an effective grounding network that creates a low-resistance path to the ground.</p>
                <h3 style="color:blue;">GROUNDING ELECTRICITY</h3>
                <p>Copper and Aluminium materials create a low-resistance path to the ground for the lightning’s potentially dangerous electricity – components and materials are specially manufactured for lightning protection. When a lightning protection system is in place it intercepts the strike, directing it safely to the ground without impacting a building’s structure or contents.

                        </p>
                       
                     <p>Today, lightning protection systems are built into the structural design of many homes, public buildings and business premises. Protecting buildings, their occupants and contents they have become integral to a building’s design.</p>
                     <h3 style="color:blue;">SURGE PROTECTION</h3>
                     <p>According to standards , post lightning strike on a building having an external lightning protection , impulse current is dissipated through a low impedance path (down conductors to the earth termination system). Part of lightning /impulse current gets dissipated into the earth and part of its gets coupled into the building through other utility services example power lines, data lines, metallic pipelines etc.<br>
                        As per standards, lightning strike occurs in a few microseconds. The normal protective device like, fuses, MCBs etc respond to short circuit current or over load current lasting few milliseconds. Since the lightning overvoltages are typically limited to few microseconds, the normal devices are unable to protect against these over voltages.
                        Hence, new class of protective devices (SPD) which respond in a few microseconds are needed to protect the equipment.<br>
                        A surge protection device on its own can’t protect a structure from a direct lightning strike due to the massive volume of electricity – the grounding network for lightning must also be implemented.

                            </p>
