            <p>The AC power supply is a common <a style="color:#479fe0;" href="https://www.jeftechno.com/blog/sources-of-switching-surges.php"> source of surges;</a> unfortunately, the human eye cannot detect it. It is especially harmful in India, where overhead lines provide the majority of our power. As per IS/IEC 62305, Source S3 is Flash to the service line which has got all the loss, risk, and damages as in the case of flashes to the structure (Source S1). Hence, it is essential to note that any cable entering electronic equipment is also an<a style="color:#479fe0;" href="https://www.jeftechno.com/blog/automated-lightning-risk.php"> easy path for the lightning current to enter and cause unexpected & untold damage.</a> In addition, when the lightning hits the structure, approximately half of the lightning current reaches the services. A power line is a common utility in a structure, regardless of its usage, which can range from a simple house to an advanced IT facility. Data lines and water/sewage metal pipelines are the least common lines.</p>
           <p>Surge or lightning protection devices safeguard electrical equipment from damage caused by over-voltage transient occurrences by blocking or diverting surge current to the ground rather than flowing through it. Surge protectionis attached to a conductor on the equipment's line side.</p>
           <p>Sources for the surges on mains supply:</p> 
           <h4>SURGES DUE TO MOTHER NATURE:</h4>
           <p>A) Predominant route is the lightning current through over head power line, which will also induce currents into signal cables entering the building.<br><br>


            B) Opposite is also true. Lightning current through signal cables can also induce current in power line.</p>
             <h4>Manufactured SURGES:</h4>
             <p>C) Due to inductive effects or the transformer's inadequate regulation, heavy current load switching by the power board can create severe surges on the supply.<br><br>


                D) Lift motors and other large electrical machines can generate significant surge, especially when THE MOTOR IS WORN.<br><br>


                E) Capacitor bank switching<br><br>


                F) Resonating circuits associated with switching (eg. Thyristors)<br><br>


                G) Faults such as short circuit and arcs to the earth.</p>
                <p>H) In residences, it has been observed that operating refrigerators on or off through thermostats create problem in sensitive electronic equipment like music systems, Computers etc.</p>
                <p>In simple terms, whenever a fuse blows, resulting in sudden interruption of current flow, surges are generated, which will affect sensitive electronic equipment and components like IGBT.</p>
                <p>Even a simple ON/OFF operation via a thermostat can cause problems in sensitive electronic equipment in a home. In an industrial setting, the amount of surges absorbed by PLC/DCS/Servers/EPABX is easily imaginable.</p>
                <p>For more information on this subject IEC 61000- EMC- Part 4- Testing & measurement techniques- Electrical Fast Transient (EFT)/burst immunity test, which is a basic EMC publication shall be referred apart from IS/IEC 62305-Protection against Lightning, Parts 1 to 4.</p>
