            <div class="post-entry">
            <h4 style="color:#479fe0;">DOES IT DEPEND ON CLASS/TYPE OR COMPONENT OR RECOMMENDATION BASED ON STANDARDS BODY?</h4>
            <p>Often customers are confused by different vendors, forcing them to believe that their product is the best in the world. When it comes to “Concept selling product” like SPDs, customer depends more on the vendors as there are many new technologies & standards. Some vendors try to influence the customer with their product without giving any proof or supporting document from Standards, just because they were in this field for a long time & have presence all over the world.!!!</p>
            <h4 style="color:#479fe0;">THIS ARTICLE GIVES A CLEAR PICTURE ABOUT THE SELECTION OF SPD FOR MAIN DB WITH PROOF FROM IEC STANDARDS.</h4>
            <p>After going through it, you will realise that<a style="color:#479fe0;" href="https://www.jeftechno.com/blog/spd-selection-and-installation-myths-and-fact.php"> selection of SPD</a> for main DB is such a simple task & why confusion prevailed over the years.</p>                                                                                                                                                                                                                                                                                                                                
            <h4 style="color:#479fe0;">SELECTION OF LV SPD FOR MAIN DB <u>“DEPENDS ON”:</u></h4>
            <p>Voltage Protection level of the SPD (when compared to voltage withstanding level of the equipment).
               <br> Impulse discharge current of SPD connected between each phase to neutral (R-N, Y-N & B-N).<br>
                Impulse discharge current of SPD connected between neutral to protective earth (N- PE).<br>
                <br>
                It is enough to have an SPD with Iimp of 12.5 kA for each L-N & 50 kA for N-PE, with connection type CT-2 (i.e., 3+1 configuration- 3 modules from L-N & 1 module between N-PE). Whichever product meets this specification shall be used in the main DB “irrespectiveoftheClassofSPD and thecomponentusedinit”</p>
                <h4 style="color:#479fe0;">PROOF:</h4>
                <p><b>IS/IEC 61643-12:</b> SPDs connected to low voltage power distribution system- Selection & application principles (page 52, Class 6.2.1.3: In, Imax, Iimp)<br>

                <b>IEC 60364-5-53:</b> Electrical installation of buildings-Selection & erection of electrical equipment- Isolation, switching & control (page 23, Clause 534.4.4.2 Class I tested SPDs,Table 534.4-Selection of Iimp where the building is protected against direct lightning strike.<br>

               <b>IS/IEC 62305:</b> Protection against lightning, part 1: General principles (page 64, Table E.2- Expected surge over currents due to lightning flashes on low voltage systems.<br>

                In fact, if the risk assessment is carried out according to IS/IEC 62305-2, the value of Iimp is only 10 kA for the most severe LPL- Lightning Protection Level- I for the source of damage S3 (direct flash on the service- I.e power line)

</p>
                <h4 style="color:#479fe0;">SELECTION OF LV SPD FOR MAIN DB “DOES NOT DEPEND ON”:</h4>
                <p>Component used- whether Spark gap or MOV<br>
                Class I or I+II (or) Class B or B+C (or) Type 1 or 1+2.<br>
                <br><br>
                Class I, I+II are used by IEC. (International Electro-technical Commission) - India is an IEC member.<br>
                
                Class B, B+C are used by Germans (India is far away from Germany)<br>

                Type 1 or 1+2 is used by Europe (India is far away from Europe)<br>

                Some vendors try to add some features in their SPD just to avoid competition without any proof from standards.<br>

                <b>For example,</b> Class I SPD with inbuilt fuse (which has all the disadvantages) or Class I SPD which can be directly mounted in bus bars, when a simple “V” type wiring can reduce the voltage protection level.<br>

                <b>Do not fall prey to monopoly products & ask JEF for the right product with proof from standards.!!!<b></p>
