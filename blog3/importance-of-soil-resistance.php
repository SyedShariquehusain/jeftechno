            <p>Further, the resistivity of the soil cannot stay uniform all throughout the life as the soil can be eroded due to wind, rain, floods, manmade actions (excavations) etc. So the<a style="color:#479fe0;" href="https://www.jeftechno.com/blog/interconnection-of-earth-pits.php"> earth pit</a> resistance cannot be compared with the measured resistivity during the design /construction phase and the evaluation should be done by measuring the resistivity of the soil along with the measurement of the earth pit resistance at the time of acceptance testing.</p>
            <p>The resistivity of the soil normally varies for the different layers. As we go deeper, the resistivity of the soil may not vary much seasonally / changes in environmental conditions. Hence, the variation in resistivity of the top layers needs to be considered for earthing design.</p>
            <p>The resistivity of the soil is dependent on salt content, moisture and temperature and varies as per the graph below.</p>
