            <img src="../assets/images/blog/imp-picture-3.png" alt=""/>
            <p>Parameters impacting the soil resistivity measurement<br>
                Soil resistivity measurement should be carried by using a quality meter which complies with the international standards and as per IEEE 81 Annex E. The measurement is to be carried by a meter which is having a capability of injecting the current at variable frequency and with variable voltage. The readings measured with low quality meters will not show accurate readings. Factors affecting measurement are:</p>
            <p>- Presence of loose/backfilled soil<br>
                - Presence of any buried metals like water, gas, other process pipelines and cables.<br>
                - Presence of live HT lines above the test setup may have an induced current in the cables connecting the spikes and may result in errors in measurement and also potential hazard to the personnel working on the test setup.<br>
                - Presence of water table near the measurement area or presence of any earth gird of a substation will have an impact on the measured values.<br>
                - To obtain accurate measurement, the distance of any such earth grids and water table from the soil resistivity measurement area should be at least the spacing between the spikes.</p>
                <img src="../assets/images/blog/imp-picture-4.png" alt=""/><br>
                <img src="../assets/images/blog/imp-picture-5.png" alt=""/>
                <p>Soil resistivity cannot be considered based on the average of values for the varying depths.</p>
                <p>Readings should be measured at different depths in a given direction and compared with the previous reading of the same direction to check the extent of variation. If there are any abnormal variations in the readings, the measurements may be repeated / additional measurements done for the intermediate depths. Typically, the largest variation in a given direction should be less than 30% for the soil to be considered as uniform.</p>
                <p>If variation is beyond this limit (which is true in most cases), the soil is deemed to be non uniform and multilayer soil model shall be used for designing. The number of layers will depend on the measured values and the criteria shall be to have as close match as possible with the measured values (As low an RMS error as possible).</p>
                <p>While using a simple average (arithmetic mean) of measured soil resistivity values for design, the margin of error in safety parameters due to soil resistivity can easily exceed 100%.</p>
                <p>The standards have provided formulas to determine approximate values of touch and step potentials for uniform layer and two layer soils and for multi-layer soil it is necessary to go in for software simulation models as prescribed in the IEEE 80 Annex H. The CDEGS software by SES has multi layer modelling capability and provides accurate and optimum design with least possible error.</p>
                <p>Measurement of resistivity at a location should be done depending on the function / purpose of the proposed earthing system. If standard earth pit of 3mtrs are to be installed (normal industrial plants, commercial / residential buildings), then the soil resistivity of upto a depth of 10mtrs is required. For a substation grid, the resistivity to a depth not less than the length of diagonal of the grid should be measured.</p>
                <p>Hence, an accurate measurement of soil resistivity is important in ensuring an optimum grounding design.</p>
