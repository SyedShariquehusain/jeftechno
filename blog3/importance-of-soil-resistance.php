            <h4>IMPORTANCE OF SOIL RESISTIVITY IN THE DESIGN OF EARTHING FOR THE SAFETY & RELIABILITY</h4>
            <p>Soil resistivity is a measurement of the resistance or electric current that the soil conducts.The design of a system based on passing current across the surface of the earth is a key component.</p>
            <p>Soil resistivity plays a vital role in the design of an earthing system. For substations, the key design acceptance (safety) criteria of touch and step potentials soil resistivity are greatly influenced. For other<a style="color:#479fe0;"  href="https://www.jeftechno.com/grounding-systems.php">grounding systems</a> such as industrial plants / commercial and residential buildings, the acceptance criteria of overall grounding resistance and current density on the electrode surface are also dependent on soil resistivity. Hence, the accuracy of the design depends on the accuracy of the measured Soil resistivity values.</p>
            <p> As per standards, the soil conditions up to 3 to 4 times the longest dimension of the mat affect the current dissipation. The soil generally is not uniform all through the depth & breadth and varies with topography.</p>
            <p> Soil resistivity can be measured in a variety of methods. Electrical profiling is the best approach for assessing<a style="color:#479fe0;"  href="https://www.jeftechno.com/blog/soil-treatment-for-good-earthing.php"> soil resistivity for the design of earthing and safety requirements.</a> By injecting a current, and monitoring the voltage drop at different spacing, without any excavation electrical profile of the soil can be done. Wenner's Four-Point Method is the most popular and user-friendly method.</p>
            <p>In Wenner Four point method the resistivity is measured by using four spikes with equal spacing connected to the soil resistivity meter. The extreme spikes are called as current spikes and the inner spikes are called as potential spikes. The current will be injected through the extreme spikes. There will be a potential gradient in the mean area and the drop in the potential is measured between the potential spikes. The meter will display the resistance which can be used for the interpretation of resistivity of the soil by the established formula.</p>
