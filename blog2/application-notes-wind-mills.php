              <h2>APPLICATION NOTE – WIND MILLS| LPS & SPD|</h2>
              <h2></h2>
            </div>
            <div class="post-media"> <img src="../assets/images/blog/Application Note - Wind Mills.jpg" alt="" width="800" height="370"><span class="event-calender blog-date"> 21 <span>june</span> </span> </div>
            <div class="post-entry">
            
            <p><b>Application Note: Lightning protection & Surge protection requirement for Wind Turbines as per IEC 61400-24.</b></p>
            <p> A windmill is a device that uses sails set on a spinning shaft to harness the energy of the wind.The panels are rigged at an angle or with a slight twist, splitting the wind force into two components, one of which causes rotation in the sails' plane.</p>
            <p>India’s economy is growing rapidly in a vibrant mode and in an efficient way, which in turn demands huge uninterrupted energy supply. Utilization of regenerative energy is on a steady rise over the past two decades. Windmills, Solar PV & bio-gas plants are getting popular day by day. This high market potential is not only for the energy sector but are equally available for the Electrical & Electronic equipment suppliers worldwide.</p>
            <p>In this application note, let us talk about windmills, which are megawatt plants. The buy-back period is only a few years, and any stoppage will have a crucial impact on the return on investment, downtimes are expensive for owners of such high-value installations.</p>
            <p>IEC 61400-24: Lightning Protection of Wind Turbines is the basis for the solution.</p>
           
            <p>There are two common challenges for the windmills.<br><br>
            1-They are <b>much taller</b> than the surrounding environment (100 m & above)<br><br>


            2-<b>The concentration of electrical & electronic system </b>is confined in a small area
            </p>
            <p>The electrical & electronic system includes switchgear cabinets, motors, drives, frequency converters, actuators, sensors, control electronics including communication bus systems employing Modbus or Profibus protocol via RS 485 physical layer of popular ISO/OSI 7-layer model are followed worldwide.</p>
            <p><b>Lightning surges</b> from Mother Nature & <b>switching surges</b> from radiation or generated internally & propagated through metallic structures pose a huge problem to windmill electronics as “height” of the windmill and exposed nature of installation plays an important role in inviting surges.</p>
            <p>The annual number of lightning strikes (cloud to ground) is determined from isokeraunic level. NBC 2016 also has this data for major Indian cities. As windmills are taller than 100 m, upward flashes in the form of ground to cloud flashes is also a possibility, apart from cloud to ground flashes. In addition, ground to cloud flashes starting from high exposed objects carry higher charges of lightning current because of which, due importance has to be given to the protection measures of rotor blades apart from suitable SPD measures.</p>
            <p>Shielding procedures and a global earth termination system are critical for the protection of electronics inside windmill Lightning protection zone designs, ensuring that the entire system runs inside a predetermined EMC (Electro Magnetic Compatibility) environment.</p>
            <p>LPZ (Lightning Protection Zone): This is a concept in which conducted and radiated interference are gradually reduced from coarse to choice(sensitive) electronics using SPDs and LPS in the form of a Faraday cage. Ensuring that the voltage withstanding level of each piece of equipment never exceeded, even in the event of a lightning strike. This is accomplished using the "insulation coordination strategy," in which each entry-level SPD uses shielding strategies to protect the next-level SPD. In addition to safeguarding the electrical and electronic equipment in their zone. There are four Lightning protection zone in windmill systems.</p>
            <p><b>LPZ O A :</b> The area above the<a style="color:#479fe0;"  href="https://www.jeftechno.com/blog/how-lps-work.php"> LPS installation </a>(electric & magnetic field are maximum)<br><br>


                <b>LPZ O B :</b> The area just below the external LPS which is covered by rolling sphere radius. (first level of attenuation)<br><br>


                <b>LPZ 1:</b> Entry point of power & signal cables from the windmill tower to the building. (second level of attenuation)<br><br>


                <b> LPZ 2:</b> Place where sensitive electronic equipment is located. (final level of attenuation where electric & magnetic fields are very minimum and will be unable to cause any upset to the equipment)

            </p>
            
            <img src="../assets/images/blog/111.png" alt="" width="400" height="400"/><br><br>
            <img src="../assets/images/blog/112.png" alt="" width="400" height="400"/>
            <h4><u>Figure 1: Wind turbine LPZ concept</u></h4>
            <p><b>Shielding: </b>Nacelle are made of metal which is a closed structure by itself to protect from radiated effects of lightning. The attenuation of EM field is maximum inside this closed metal volume when compared to high level of EM field outside the metal surface. Switchgear and control cabinets are also made of metal for the same purpose. Shielded cables shall be used which has 360-degree bonding at entry & exit points which is connected to EBB (Equipotential Bonding Bar) on either side of the cable. EBB is referred as MET- Main Earthing Terminal in IEEE standards.</p>
            <p><b>Earthing:</b> “Global earthing” as explained in the IS3043 latest edition (2018) shall be followed which includes the structural steel reinforcement of wind turbine with the earth grid surrounding the wind mill & the building, which is located outside the windmill.</p>
            <img src="../assets/images/blog/113.png" alt="" width="400" height="400"/>
            <h4>Figure 2: Global earthing concept.</h4>
            <p>SPDs: While radiated surges are limited by shielding technique, conducted surges are reduced below the withstanding level of equipment by using<a style="color:#479fe0;"  href="https://www.jeftechno.com/blog/power-line-and-data-line-spd-protection.php"> SPDs for power, data, and communication lines.</a></p>
            <p>Either Class I or Class I+ II 3 phase SPD with 400/690 V compatible with minimum Iimp of 12.5 kA for TN-C system shall be employed in parallel at the main DB taking care of the installation technique strictly recommended as per the Installation</p>
            <p>Instruction of<a style="color:#479fe0;" href="https://www.jeftechno.com/surge-protection-devices.php"> SPD</a> manufacturer. TN-C system is one, in which neutral & protective earth are combined to form PEN, which is 4 wire system (R, Y, B & PEN). SPD shall have local & remote indication in the form of relay contact with NO/NC points which can be connected to hooter or LED or as a contact input of the PLC system.</p>
            <p>For more information, contact us at <a  style="color:#479fe0;" href="mailto:@jeftechno.com">marketing@jeftechno.com"</a></p>
