            <h4>WAYS TO PROTECT CNC MACHINE'S ELECTRONICS FROM SURGES & AVOIDING EPROM CORRUPTION</h4>
            <p>CNC stands for computer numerical control, which is the automated control of machining instruments like drills, lathes, mills, and 3D printers using a computer.</p>
           <P>The introduction of CNC machines during the 1950s has radically changed the manufacturing industry. Now, 3 D printing also has been introduced. These machines are far, more, complex.</P>
            <P>Depending upon its’ application, CNC machines have different types of electronics ranging from - dedicated electronics /PLCs / Display units with or without touch screens , interface to PC etc. etc.</P>
                <p>In a sense, these new age machines represent a special segment of industrial robot system, as they are programmable to perform many kinds of machining operations (within designed physical limits, like other robotic systems).</p>
                    <p>The primary function of CNC machines i.e, precise and automatic motion control, makes this sophisticated machine so popular.</p>
                    <p>Today we hear more about 24 X 7 everywhere. Due to international competition & commitments, every industry wants to increase the availability of their plant so that the production targets are achieved .On one hand ,the size of the components of CNC machine becomes smaller and smaller every day, from room size equipment to small size cards. Though it is convenient because of its’ small size, on the other hand, the energy level it can withstand has reduced drastically. For e.g. an IC can withstand energy only up to 1 micro joule , whereas a transistor can withstand up to 10 micro joules.</p>
                    <p>Failures in CNC machine happen due to two important disturbances (variables present) in low voltage network - steady state errors and transient errors. <br><br>Steady state errors mainly comprise of over voltage which lasts several cycles (swell) , harmonics, RFI/EMI effects etc. <br><br>Transient errors comprise of transient over voltages i.e, Lightning surges and switching surges due to operations like welding, capacitor bank switching etc.</p>
                        <p><b>The two important reasons for failures due to lightning</b> <br><br>–direct hit, (in which the damage is visible because what we see, we believe ). But direct lightning hit to an equipment is very rare because CNC machines are installed inside a structure & is fed by sub distribution boards, whereas only main DB bears the brunt of direct lightning strike in lightning prone areas. The other reason, often un-noticed is, distant lightning. In this case, the lightning current is carried by all types of cables except optic fiber cables. Hence many of us have experienced that, immediately after thunder storm & showers, our phone line stops functioning or our sophisticated machines do not work due to electronic card failure. The myth here is, we have general feeling that when the telephone or machine is not in use i.e, when they are in “switch off” condition, they are safe. But the fact is , they would have been safe if its’ connections are physically removed from the respective sockets. These safety tips are available in “installation manual” of TV, Refrigerator, washing machine etc, but we seldom read. In manufacturing plants, which work 24 hours a day, how do we achieve this? The only solution is to protect them by using <a style="color:#479fe0;" href="https://www.jeftechno.com/surge-protection-devices.php"> SPDs –Surge Protection Devices.</a>
                                                    </p>
                    <p>Before discussing about SPDs, let us first understand what is surge and how it is going to affect our CNC machines, so that it will be easier to install appropriate SPDs for protection of CNC machine.</p>
                    <p>Put in simple terms, a surge is a sudden increase in current and voltage for a short period (a duration of 350 microseconds during lightning and 20 microseconds during switching). There are various sources of surges. Artificial transient surges occur due to brownouts, blackouts, capacitor switching, frequent switching on/off of heavy-duty equipment, welding, electrostatic discharge, etc. Mother Nature generates two types of surges: direct lightning strikes, which are extremely rare, and distant lightning-induced indirect surges, which are common.</p>
                    <p>Consider a load cell signal, whose wires run alongside a 415 V AC, three-phase power line, or a CCTV signal cable, which runs alongside 11 or 33 kV HT cable, both of which are common in industrial settings. Due to electromagnetic induction, any minor change in the power line will affect the load cell's millivolt signal. It will induce measurement errors in the millivolt signal as they operate in very low-level signals and may cause a process upset.</p>
                   <p>The result will be unwanted measurement signal observed in the DCS (Distributed control System) or PLC (Programmable Logic Controller) or SCADA (supervisory control and data acquisition), which is not due to actual measurement signal itself. </p>
                     <p>Many feel that unless they see a charred PCB they won’t accept that the problem is due to lightning /switching surge effects. The more common problem we encounter in the normal plant life is corruption of EPROM ( Erasable Programmable Read only memory).</p>                                                                                                                                                                                                                                                  
                   <p>This is visible from the sudden junk data appearing in the MMI (Man Machine Interface) panel. In general, it is not a common practice to- put that loop of PLC or DCS in manual mode, remove the corrupted EPROM , erase it in UV eraser, reprogram it and put it back into operation as this is a long drawn & time consuming process, resulting in production loss. . If we examine why such corruption of EPROM suddenly takes place, the reason will be surges.</p>
                   <p>Because, while programming is taking place, a voltage higher than the normal working voltage is applied to specific EPROM pins (so that EPROM understands that it is in programming mode and not in normal operating mode). During a surge, the same thing happens. Because a sudden high voltage appears in the pins of the IC, it believes it is in programming mode and the ones because the extra high voltage appears in the pins. This is the cause of EPROM collection. As a result, the intensity of surges is solely responsible for the failure or corruption of programs in the CNC machine. CNC machines' SMDs and electronic equipment are designed to endure a minor increase in voltage levels that falls well within the tolerance limit. This is known as equipment withstanding voltage, and it is typically 1000 V AC RMS for 1 minute for PLC and electronic equipment with a 230V AC input power supply voltage. If a surge of this magnitude occurs, nothing happens to the equipment. These levels are greater than the standard tolerance limit; over time, they are high enough to permanently fail the equipment.</p> 
                   <p>This degradation causes the equipment to reach the permanent failure level in more than 90% of the cases, according to experience. It might last anywhere from a few days to several months. However, because the problem is intermittent and manifests itself differently at different times, this is the most unpredictable problem for maintenance engineers.</p>
                   <p><b>Common Myths:</b><p>
                    <p><b>Generally we feel that SPDs are not required as we have number of (other existing) protection arrangements viz. external lightning arrestor, MCB/ MCCB, good earthing , bonding, shielding , isolation transformers, UPS, Intrinsically safe barriers, isolators etc</b></p>
                  <p>Unfortunately the purposes of all these equipment are for different reasons altogether and not for protecting the equipment from transient surges. Let us analyse the scope of each one of them.</p>
                  <p><b>External Lightning protection</b> with good earthing is for protecting the building from direct lightning strike.</p> 
                  <p><b>MCB Miniature circuit breaker or MCCB- Moulded case circuit breaker</b> is for protecting the equipments from short circuit due to flow of power frequency fault currents .MCB or MCCB operate in millisecond duration which means before a MCB or MCCB detects surges, surges would have already passed through them and damaged the equipments.</p>
                 <p><b>Earthing</b> is for both personnel safety and equipment safety.<br><br>

                    <b>Bonding</b> is to reduce the earth resistance and maintain equipotential.<br><br>

                   <b> Shielding</b> is done to protect equipments from RFI/EMI effects.</p>
                                                        
                 <p><b>Isolation Transformer</b> is mainly used to protect equipments from the faults that happen at the primary side. For example, in case of a short circuit in the primary side, the equipments connected to the secondary side will not be affected if they are connected through an isolation transformer.</p>
                    <p><b>UPS or uninterrupted power supply,</b> as the very name suggests are used to provide continuous power through battery in case of mains power failure or to regulate the power within a narrow band.</p>
                        <p><b>Intrinsically Safe barriers or Isolators</b> are used in hazardous plant areas (for example refineries, Petro- chemical plants, fertilizer plants etc) to restrict the electrical energy to very low levels, so that even in case of any short circuit the available power is too low to initiate a spark or fire.</p>
                         <p>Hence SPDs are the only protectors for the CNC machines against transient surges. Now, we can discuss the proper selection and installation of SPD for trouble free operation of a CNC machine.</p>
                            <p>Depending on the incoming supply of CNC machine i.e, 3 phase with neutral or without neutral , SPDs shall be installed with potential free contact, thermal disconnecter , including base element & pluggable arresters to be connected between Phase to Neutral (3 nos. for 3 phase to neutral ) and between neutral to protective earth. SPDs for three phase power supply (L-N), (N-E) at local or sub distribution board or branch panel should have l MOVs for L-N (WITH MCOV of 275 V to take care of power supply fluctuations & SPARK GAP for N-E. Requirement class is , Class I + II as per IEC 61643-11.</p>
