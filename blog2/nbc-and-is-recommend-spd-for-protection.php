              <h2>NBC & IS RECOMMEND SPD FOR PROTECTION</h2>
            </div>
            <div class="post-media"> <img src="../assets/images/blog/NBC & IS recommend SPD for protection.jpg" alt="" width="800" height="300"/><span class="event-calender blog-date"> 21 <span>june</span> </span> </div>
            <div class="post-entry">
           <p><b>NBC & IS recommend SPD for protection of sensitive electronic equipment from conducted surges through power & data cables.</b></p>
           <p>A variety of devices provides over-current protection, but <a style="color:#479fe0;" href="https://www.jeftechno.com/surge-protection-devices.php"> Surge protection devices</a> are the only ones that can safeguard sensitive electronic equipment from overvoltage caused by contaminated power sources and the increasing use of non-linear devices.</b></p>
           <p>Considering this, present NBC & IS have recommended SPDs</p>
           <p>Transient over voltages are generated predominantly due to the following reasons. There are many trivial reasons which are not listed here</p>

            <p>a) Whenever lightning strikes the power or signal cables directly or near the cables.<br><br>


                    b) As a consequence of faults in the power system<br><br>


            c) Switching on and off of Heavy inductive loads etc.</p>
           <p><b>Surges</b> happen due to lightning and switching actions. Before proceeding further, it is better to understand how SPDs are specified by different countries/manufacturers across the world.<br><br>


            <b>Class I, I+II, II, III </b>are the terminology used by IEC.<br><br>


            <b>Type I, I+II, II, III</b> are the terminology used by European Norms (EN)<br><br>


               <b> Class B, B+C, C, D</b> are the terminology used by Germans (VDE)

                </p>
            <p>It is wise to use 12.5 kA Iimp rated , Class I+II MOV SPD as per IEC 60364 -5-53 for L-N to avoid “follow current “ problems associated with Spark gas devices for L-N in 3+1 configuration meaning 3 MOV based SPD for 3 phase to Neutral & 1 Spark gap based module for N-PE. It is important to use Spark gap SPD with 50 kA Iimp rating for N-PE because MOV will trip RCDs after getting aged. Many vendors offer very high Iimp rating Spark gap for L-N which is not necessary & to overcome follow current problems, expensive HRC fuses are needed which are much costlier than the SPD itself.

                                                                                                                        </p>
               
               <p>It is improperly implicit that “only” Spark Gaps respond for lightning surges and MOVs respond “only” for<a style="color:#479fe0;" href="https://www.jeftechno.com/blog/sources-of-switching-surges.php"> switching surges.</a></p>                                                                                                        
           <p>SPDs are simple, passive, non-linear components which cannot differentiate between lightning and switching surges. Whether it is spark gap, GDT, MOV or SAD (Silicon Avalanche Diode), they act when the voltage across them exceeds a pre-determined level as all of them are voltage sensitive devices. It is “only” because of the Cost Vs performance, it is general practise to use Spark Gaps for lightning surges & other components ( MOV, GDT & SAD) for switching surges either alone or in combination.</p>
           <p><b># Class I+II SPDs </b>are recommended at the Main DB of all industries.<br><br>


               <b> # Class II SPDs </b>are recommended at the Sub DB of all industries.</p>    

                <p><b># Class III SPDs </b>are installed near sensitive / critical equipment. Electronic equipment like MODEM, RF communication equipment, EPABX, Weigh bridge load cell indicators etc. are few of the sensitive equipments. UPS, MCCB with communication ports are few examples of Critical equipments. Unlike critical equipment, which primarily operates on 3 phase (415 V AC) & 1 phase ( 230V AC in general, but also 110V AC in some cases in India, especially Instrumentation products used in Refineries, petrochemical industries etc.), sensitive equipment operates in wide operating voltages ( AC- 230V and 110V & DC Voltages- 5 V, 12 V, 24 V, 48 V, 110 V etc.) .

          </p>
          <p><a style="color:#479fe0;" href="https://www.jeftechno.com/spd/spd-for-power-lines.php"> Power line SPDs</a> (which are commonly connected parallel to the load) include a local indication (mechanical flag, which is more reliable than LEDs) as a standard feature and a remote signal (potential free contact) as an optional feature in terms of maintenance. Connecting SPDs in parallel has the primary benefit of eliminating the need to worry about load current (whether 100A, 1000A, or 5000A). However, series-connected power line SPDs are also available on the market, and they are not only popular, but they also have the technical flaw of disconnecting the load in the event of SPD failure.
          <br>JEF has a wide range of SPDs to suit all the above requirements.

          </p>
